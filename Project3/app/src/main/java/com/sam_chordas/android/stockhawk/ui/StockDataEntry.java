package com.sam_chordas.android.stockhawk.ui;

public class StockDataEntry {

    private int x;
    private int y;

    public StockDataEntry(int x, int y) {
        this.x = x;
        this.y = y;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
